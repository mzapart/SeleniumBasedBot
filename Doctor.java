public class Doctor {
	private String name;
	private String surname;
	
	public Doctor(String name, String surname) {
		super();
		this.name = name;
		this.surname = surname;
	}
	public String getSurname() {
		return surname;
	}
	public void setSurname(String surname) {
		this.surname = surname;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
